<template>
    <container-template>
        <tabs-menu slot="tab-menu"/>
        <div slot="tab-content">
            <h5>
                Users
                <router-link :to="{ name: 'settingsCompaniesUsersForm' }" class="btn btn-primary">
                    New User Invite
                </router-link>
            </h5>
            <ul id="myTab" class="nav nav-tabs nav-tabs-simple" role="tablist">
                <li class="nav-item">
                    <a
                        :class="{ active: listToShow == 'usersList' }"
                        data-toggle="tab"
                        role="tab"
                        @click.prevent="listToShow = 'usersList'"
                    >
                        Users
                    </a>
                </li>
                <li class="nav-item">
                    <a
                        :class="{ active: listToShow== 'newUsersList' }"
                        data-toggle="tab"
                        role="tab"
                        @click.prevent="listToShow = 'newUsersList'"
                    >
                        Invites
                    </a>
                </li>
            </ul>
            <div v-if="listToShow == 'usersList'" class="table-responsive">
                <vuetable
                    ref="Vuetable"
                    :append-params="{format: 'true', relationships:'roles'}"
                    :fields="usersFields"
                    :http-fetch="getTableData"
                    api-url="/users"
                    class="table table-hover table-condensed"
                    pagination-path="">

                    <template slot="name" slot-scope="props">
                        <span> {{ props.rowData.firstname }} {{ props.rowData.lastname }} </span>
                    </template>

                    <template slot="actions" slot-scope="props">
                        <button
                            class="btn btn-primary m-l-5"
                            @click="rowAction(props.rowData, true)">
                            <i class="fa fa-eye" aria-hidden="true"/>
                        </button>
                        <button
                            class="btn btn-complete m-l-5"
                            @click="rowAction(props.rowData, false)">
                            <i class="fa fa-edit" aria-hidden="true"/>
                        </button>
                        <button
                            :disabled="isCurrentUser(props.rowData.id)"
                            :class="{'deactivated': isCurrentUser(props.rowData.id)}"
                            class="btn btn-danger m-l-5"
                            @click="confirmUserDelete(props.rowData.id)">
                            <i class="fa fa-trash" aria-hidden="true" />
                        </button>
                    </template>
                </vuetable>
            </div>
            <div v-else class="table-responsive">
                <vuetable
                    ref="Vuetable"
                    :append-params="{format: 'true', relationships:'companies,roles',q:'(is_deleted:0)'}"
                    :fields="usersInviteFields"
                    :http-fetch="getTableData"
                    api-url="/users-invite"
                    class="table table-hover table-condensed"
                    pagination-path="">

                    <template slot="name" slot-scope="props">
                        <span> {{ props.rowData.firstname }} {{ props.rowData.lastname }} </span>
                    </template>

                    <template slot="actions" slot-scope="props">
                        <button
                            class="btn btn-danger m-l-5"
                            @click="deleteUser(props.rowData.id)">
                            <i class="fa fa-trash" aria-hidden="true" />
                        </button>
                    </template>
                </vuetable>
            </div>
        </div>
    </container-template>
</template>

<script>
import { vuexMixins, listMixins } from "@/utils/mixins";
import { mapGetters } from "vuex";

export default {
    name: "UsersList",
    components: {
        ContainerTemplate: () => import(/* webpackChunkName: "settings-container" */ "@v/settings/container"),
        TabsMenu: () => import(/* webpackChunkName: "settings-apps-tabs" */ "@v/settings/companies/tabs")
    },
    mixins: [
        vuexMixins,
        listMixins
    ],
    props: {
        currentUser: {
            type: Object,
            default() {
                return {}
            }
        }
    },
    data() {
        return {
            usersFields: [{
                name: "name",
                title: "Name"
            }, {
                name: "roles.0.name",
                title:"Rol",
                sortField: "roles_id",
                width: "30%"
            }, {
                name: "lastvisit",
                sortField: "lastvisit",
                width: "30%"
            }, {
                name: "status",
                sortField: "status",
                width: "30%",
                formatter(value) {
                    return value ? "Active" : "Inactive";
                }
            }, {
                name: "actions",
                title: "Actions",
                titleClass: "table-actions",
                dataClass: "table-actions"
            }],
            usersInviteFields:[
                {
                    name: "email",
                    title: "Email"
                }, {
                    name: "roles.0.name",
                    title:"Rol",
                    sortField: "roles_id",
                    width: "30%"
                }, {
                    name: "actions",
                    title: "Actions",
                    titleClass: "table-actions",
                    dataClass: "table-actions"
                }
            ],
            listToShow:"usersList"
        }
    },
    computed: {
        ...mapGetters({
            currentUserId: "User/id"
        })
    },
    methods: {
        isCurrentUser(userId) {
            return this.currentUserId == userId;
        },
        rowAction(userData, isReadOnly) {
            this.$router.push({ name: "settingsCompaniesUsersFormEdit", params: { id: userData.id, user: userData, readOnlyMode: isReadOnly } });
        },
        getTableData(apiUrl, options) {
            return axios({
                url: apiUrl,
                params: options.params
            });
        },
        deleteUser(id) {
            if (this.isCurrentUser) {
                return;
            }

            axios({
                url: `/users/${id}`,
                method: "DELETE"
            }).then(() => {
                this.$notify({
                    group: null,
                    title: "Deleted",
                    text: "The user has been deleted",
                    type: "success"
                });
            }).catch((error) => {
                this.$notify({
                    group: null,
                    title: "Error",
                    text: error.response.data.errors.message,
                    type: "error"
                });
            }).finally(() => {

            })
        },
        confirmUserDelete(id) {
            let dialogProps = {
                title:"Delete user!",
                message:`Are you sure that you want to delete this user?` };

            this.$modal.show("basic-modal", {
                ...dialogProps,
                buttons: [{
                    title: "Accept",
                    class: "btn-primary",
                    handler: () => {
                        this.$modal.hide("basic-modal");
                        this.deleteUser(id);
                    }
                }, {
                    title: "Cancel",
                    class: "btn-danger",
                    handler: () => {
                        this.$modal.hide("basic-modal");
                    }
                }]
            });
        }
    }
};
</script>
